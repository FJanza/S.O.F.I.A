import pyttsx3

#---------------------------TTS-FUNCION-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

def tts(texto):

    engine = pyttsx3.init()                                          # Creacion de objeto

    engine.setProperty("rate", 150)                                  # Seteo la velocidad de speech

    engine.say(texto)

    engine.runAndWait()

    return

